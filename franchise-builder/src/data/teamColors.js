export function getTeamColors(n) { return TEAM_COLORS[n] ?? null; }

export function getTeamCSSVars(n) {
  const c = getTeamColors(n);
  if (!c) return "";
  return `--team-primary:${c.primary}; --team-secondary:${c.secondary}; --team-accent:${c.accent};`;
}

export function getContrastText(hex) {
  const r = parseInt(hex.slice(1,3),16);
  const g = parseInt(hex.slice(3,5),16);
  const b = parseInt(hex.slice(5,7),16);
  return (r*0.299 + g*0.587 + b*0.114) > 140 ? "#1A1A1A" : "#FFFFFF";
}
